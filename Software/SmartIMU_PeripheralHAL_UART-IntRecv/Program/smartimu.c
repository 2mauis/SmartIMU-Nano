#include "drivers\stm32f4_system.h"
#include "modules\module_serial.h"

#include "smartimu.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UART_RecvEven( void )
{
  uint8_t recvData = Serial_RecvByte();
  if(recvData == 0x0D)
    Serial_SendStr("\r\n");
  else
    Serial_SendByte(recvData);
}

void SIMU_Init( void )
{
  HAL_Init();
  SIMU_GPIO_Config();
  SIMU_UART_Config(UART_RecvEven);
}
/*====================================================================================================*/
/*====================================================================================================*/
void SIMU_Loop( void )
{
  while(1) {
    LED_G_Toggle();
    delay_ms(100);
  }
}
