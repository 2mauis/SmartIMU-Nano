#include "drivers\stm32f4_system.h"

#include "smartimu.h"
/*====================================================================================================*/
/*====================================================================================================*/
void LED_B_BLINK( void )
{
  LED_B_Toggle();
}

void SIMU_Init( void )
{
  HAL_Init();
  SIMU_GPIO_Config();
  SIMU_TIM3_Config(LED_B_BLINK);
}
/*====================================================================================================*/
/*====================================================================================================*/
void SIMU_Loop( void )
{
  while(1) {
    LED_G_Toggle();
    delay_ms(200);
  }
}
